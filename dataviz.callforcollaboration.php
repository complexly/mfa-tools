<h2>Introduction</h2>
<p>Data visualisation is key in transmitting the results of urban metabolism
research to a wider audience. There are many ways to visually represent urban
metabolism data, each with its own benefits and drawbacks. However, we feel it
is time to take a systematic look at the different data visualisation options
and tools and and determine which forms of visualisation are most useful - to
whom, for what purpose and how so?</p> 

<h2>Research questions</h2>
<ul>
  <li>How are urban metabolism data typically visualised?</li>
  <li>What tools are available to facilitate the creation of urban metabolism data visualisations?</li>
  <li>Which urban metabolism visualisations are the most suitable?</li>
  <li>Most suitable for which purposes?</li>
  <li>Most suitable for which specific audiences?</li>
  <li>Most suitable for ease of creation?</li>
  <li>What attributes determine visualisation ‘suitability’?</li>
</ul>
<h2>Method & Timeline</h2>

<p>Our method may adapt as the project evolves. However, the preliminary
workplan is to review data visualisation theory as well as different data
visualisation techniques and their characteristics. Open source visualisations
will be collected, curated and presented to various audiences, who will then be
surveyed about their experience of the visualisations. To complement the
collection of available images, we intend to produce a number of visualisations
using the same urban metabolism dataset, to see how people react to these
different options.</p>

<p>We aim to finalise our method by the end of October. In November we will
collect and develop different data visualizations and present them to chosen
audiences. Write-up will be completed in December, with Journal submission and
information dissemination underway.</p>

<h2>Outputs</h2>
<ul>
<li>A journal article, which relates data visualisation theory with urban
metabolism and details the outcome of this research project, submitted to a
relevant journal.</li>
<li>A web page of interesting and useful urban metabolism visualisations </li>
<li>A number of blog posts about the research, located in local and global urban-oriented websites as teasers for the web page and journal article</li>
</ul>

<h2>Interested? Questions?</h2>

<p>Give us a shout! We are currently looking for other contributors. Our
current scope is focused on Cape Town, South Africa - however, we ideally hope
that such experiments with visualisation could by run in different cities and
countries, using the same visualisation styles to see if reactions vary in
different settings. We welcome contributions in any form, so if you have any
other thoughts or ideas, please let us know. </p>

