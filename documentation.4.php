<p>
  The data collection process is often the most important part of performing an MFA. You can 
  get data from many different sources, and the effort and time it requires is different for 
  every MFA. The consistency and quality of official statistics may be lacking and make it 
  difficult to use these as sources for your MFA. You may also find that the data you need is 
  actually reported in the right format for the right area and time period that you require, so 
  data collection could consist of just downloading spreadsheets from one single source. 
</p>

<h2>Types of sources</h2>

<p>
  Here are some ideas to get you started. At some point we would like to set up a more 
  extensive list with details for each country and each different scale of study, but for now
  this list can give you an idea of where to look (by the way: if you would like to help out
  adding content like this, <a href="page/contact">be sure to be in touch!</a>).
</p>

<ul>
  <li>
    <strong>National Statistical Agencies:</strong>
    Statistics are gathered in virtually every country by a governmental agency in charge of
    undertaking surveys to gather this data. Start at this agency first to see what they have.
  </li>
  <li>
    <strong>EUROSTAT</strong>: 
    For many countries in Europe, reporting is done to EUROSTAT in a standardized format. This 
    kind of data is very suitable for Material Flow Analysis because there is an often-used
    <a href="publication/147">framework</a> set up to work specifically with this data. 
  </li>
  <li>
    <strong>FAOSTAT</strong>: 
    The Food and Agriculture Organization of the United Nations have a lot of information on 
    biomass in their <a href="http://faostat3.fao.org/">Statistical Division</a>.
  </li>
  <li>
    <strong>UN Comtrade</strong>: 
    The <a href="http://comtrade.un.org/">UN Comtrade Database</a> provides a very detailed
    trade database covering 3.1 billion trade records from 1962. 
  </li>
</ul>

<h3>Urban Scale</h3>

<p>
  If your MFA is done on an urban scale, then national statistics are often not detailed enough
  to provide you with the required information. <a href="tags/11/urban">Studies on an urban level</a>
  have been done throughout the world, but there is no single solution for data collection. Instead,
  you will see that different studies use different approaches to get the data, depending on the city
  and the available data. Some ideas include the following:
</p>

<ul>
  <li>
    Freight statistics. Because imports and exports into a city are not monitored by customs, freight
    statistics provide an alternative to get information on import and export flows.
  </li>
  <li>
    Academic studies done on particular materials or types of flows allow you to either find data, or 
    to check if the information you found on major flows indeed makes sense.
  </li>
  <li>
    City statistical agencies could exist and may be able to provide you with data on the city of study.
    Even if there is no statistical agency in your city, the city government may still report particular 
    statistics (emission statistics are often reported on city level, for instance).
  </li>
</ul>

<p>
  To get an idea of what kinds of sources were used by other authors, review the <a href="publication/162">
  Material Flow Analysis on City of Hamburg</a>, which provides a list of data sources 
  for each flow in the appendixes.
</p>

