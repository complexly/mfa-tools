<h1>MASTERCLASSES Designing With Flows </h1>
<h2>Towards an Urban-Metabolic Agenda for a Circular Future</h2>
<p>OVAM in collaboration with .Fabric, Vlaamse Milieumaatschappij (VMM/MIRA), Team Vlaams Bouwmeester and Metabolism Of&nbsp;Cities present a first series of masterclasses. The series take place from early December 2016 to February 2017 in the Atelier Vlaams Bouwmeester, against the background of expo and program of the Atelier Brussels &ndash; The Productive Metropolis.</p>
<p><strong>Context</strong></p>
<p>The concept of &ldquo;urban metabolism&rdquo; is fundamental to developing sustainable and circular cities. Urban metabolism is based upon the idea of directly comparing cities to living organisms. However, unlike living organisms and natural ecosystems, cities in general have a linear metabolism importing large quantities of natural resources and generating significant amounts of waste and pollution.</p>
<p>In practice, the study of an urban metabolism involves &ldquo;big picture&rdquo; assimilation and analysis of inputs, outputs, the storage of materials, nutrients, water, energy and any other flows concerning an urban region or city which can provide urban sustainability indicators and data. While the concept of urban metabolism is now gaining momentum in the spheres of academia and government, there are still many aspects that remain grey areas which need to be explored in order to understand the metabolic quantification that is relevant for urban and social economic policies and environmental planning. More specifically, the spatial perspective of urban metabolism has yet to receive the attention it deserves which poses the question: how can we apply the attributes and advantages of flows to urban life through spatial design? Great efforts are still required to establish urban metabolism as an integral part of urban planning and design, and this requires the design community to become far more enlightened about all the different kinds of flows. The challenge ahead therefore becomes to &ldquo;design the urban metabolism&rdquo; of circular, inclusive and resilient cities.</p>
<p>The Flemish, Brussels and Dutch governments and the other &ldquo;Prime Movers&rdquo; are currently setting up experiments where the urban metabolism approach offers a framework that connects circular challenges for cities and regions to spatial development and creates the opportunity for intervention at many different levels. At the same time this framework offers a number of guidelines to help make the much needed shift from linear and causal planning to a more open planning approach. Its complex nature and very definition make it &ldquo;a work in progress&rdquo;. Work is already underway in the Genk region (Limburg), the Brussels metropolitan region and in Brussels Noordrand (Buda), while the Antwerp metropolitan region and Gent are also about to begin using this concept for economic, environmental and spatial planning.</p>
<p>Our masterclasses are intended to familiarize the attendees with the theory, concept and practice of urban metabolism. With these Masterclasses we aim to stimulate the development of an urban-metabolic agenda (not limited to any urban area in particular) and also create participation and co-operation at the various spatial and inter-governmental levels.</p>
<p><strong>For whom?</strong></p>
<p>The masterclasses are open to everybody interested in getting familiar with the idea of urban planning and design through the lens of metabolism. They are recommended for city makers, urban and regional administrations (environment, urban planning, economy, etc.), professionals (construction, material management, design, etc.), academic and researchers, consultants, and everybody who aims to embrace the principles of circular economy in their activity or domain.</p>
