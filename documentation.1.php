<p>
  To get started, you need to create an account on our website. This takes one minute and 
  is very easy! If you go to the <a href="omat/add">Create your project</a> page you will be 
  prompted to provide your username and set a password. After filling this out, your account 
  will be instantly created and you can log in. 
</p>

<p>
  The next page provides you with the following options:
</p>

