  <img src="img/documentation/new-dataset.png" alt="" />
</p>

<p>
  Please take into account the following:
</p>

<ul>
  <li>
    You can set the type of MFA to either a National Economy-Wide MFA or an Urban Economy-Wide MFA. 
    There are currrently no differences in how the system processes your data; the same material 
    groups will be loaded and the same indicators will be generated. Some additional indicators will 
    be added to urban-scale MFAs in the future.
  </li>
  <li>
    If you include Data Quality Indicators, the system will prompt you to mark the quality of each
    datapoint. This helps with understanding the uncertainty of your data. 
  </li>
  <li>
    Tracking data on multiple scales is useful if you study not one particular area, but multiple regions
    (for instance the city, and the province around the city).
  </li>
  <li>
    If you choose to manage contacts and sources, OMAT provides you with a back-end system where you can
    keep track of the people you meet or are in contact with, and the sources (governmental reports, PDF
    files, studies, etc.) that you use to gather your data. It allows you to keep track of the work behind
    your MFA.
  </li>
  <li>
    In addition it is possible to keep track of the time spent on your work. This is useful if you want to
    measure your productivity and get an understanding which datapoints took a lot of time and which datapoints
    were quick and easy. OMAT will provide an in-depth analysis if the time spent, including a breakdown 
    of how much time you spent on different activities (e.g. reading, calculating, or transportation). 
  </li>

</ul>

<p>
  You can always change these settings after creating your dataset. Once the dataset is created, you will
  be taken to your dashboard from where you can manage your project. 
</p>

